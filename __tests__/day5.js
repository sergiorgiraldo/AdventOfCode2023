
const lib = require('../solutions/lib/day5');

test("getLowestLocation", () => {
	const lines = [];
	lines.push("seeds: 79 14 55 13");
	lines.push("");
	lines.push("seed-to-soil map:");
	lines.push("50 98 2");
	lines.push("52 50 48");
	lines.push("");
	lines.push("soil-to-fertilizer map:");
	lines.push("0 15 37");
	lines.push("37 52 2");
	lines.push("39 0 15");
	lines.push("");
	lines.push("fertilizer-to-water map:");
	lines.push("49 53 8");
	lines.push("0 11 42");
	lines.push("42 0 7");
	lines.push("57 7 4");
	lines.push("");
	lines.push("water-to-light map:");
	lines.push("88 18 7");
	lines.push("18 25 70");
	lines.push("");
	lines.push("light-to-temperature map:");
	lines.push("45 77 23");
	lines.push("81 45 19");
	lines.push("68 64 13");
	lines.push("");
	lines.push("temperature-to-humidity map:");
	lines.push("0 69 1");
	lines.push("1 0 69");
	lines.push("");
	lines.push("humidity-to-location map:");
	lines.push("60 56 37");
	lines.push("56 93 4");
	expect(lib.getLowestLocation(lines)).toBe(35);
});

